#[derive(Default, Debug)]
pub struct RectF32{
    pub x: f32,
    pub y: f32,
    pub w: f32,
    pub h: f32
}



#[derive(Default, Debug)]
pub struct Vector2{
    pub x: f32,
    pub y: f32,
}

#[derive(Default, Debug)]
pub struct Vector3{
    pub x: f32,
    pub y: f32,
    pub z: f32,
}

#[derive(Default, Debug)]
pub struct Vector4{
    pub x: f32,
    pub y: f32,
    pub z: f32,
    pub w: f32,
}